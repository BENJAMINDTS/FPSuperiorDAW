package com.mycompany.sumas;

public class Sumas {

    /**
     * @param args
     */
    public static void main(String[] args) {
        //inicio de variables
        int n1 = 2, n2;
        n2 = n1 * n1;
        System.out.println(n2);
        n2 = n2 - n1;
        System.out.println(n2);
        n2 = n2 + n1 + 15;
        System.out.println(n2);
        n2 = n2 - n1;
        System.out.println(n2);
        n2 = n2 % n1;
        System.out.println(n2);
    }
}
