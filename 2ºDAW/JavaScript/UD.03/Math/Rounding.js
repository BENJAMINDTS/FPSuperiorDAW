// Rounding Numbers in JavaScript
let number =  7.8
//Using Math.round() to round the number to the nearest integer
let roundedNumber = Math . round ( number )
console . log ( roundedNumber ) // 8

//Useing Math.floor() to round the number down to the nearest integer
let flooredNumber = Math . floor ( number )
console . log ( flooredNumber ) // 7

//Using Math.ceil() to round the number up to the nearest integer
let ceiledNumber = Math . ceil ( number )
console . log ( ceiledNumber ) // 8