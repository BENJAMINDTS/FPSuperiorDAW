console.log("VAmos a declarar una variable inicial que sera el numero 5");
let numero = 5;
console.log("El valor inicial de la variable es: " + numero);

console.log("Ahora vamos a sumar 3 al valor inicial");
numero += 3; // Equivalente a numero = numero + 3
console.log("El nuevo valor de la variable es: " + numero);

console.log("Ahora vamos a restar 2 al valor actual");
numero -= 2; // Equivalente a numero = numero - 2
console.log("El nuevo valor de la variable es: " + numero);

console.log("Ahora vamos a multiplicar el valor actual por 4");
numero *= 4; // Equivalente a numero = numero * 4
console.log("El nuevo valor de la variable es: " + numero);

console.log("Ahora vamos a dividir el valor actual por 2");
numero /= 2; // Equivalente a numero = numero / 2
console.log("El nuevo valor de la variable es: " + numero);

console.log("Finalmente, vamos a calcular el modulo del valor actual con 3");
numero %= 3; // Equivalente a numero = numero % 3
console.log("El nuevo valor de la variable es: " + numero);

console.log("Y ahora vamos a elevar el valor actual a la potencia de 3");
numero **= 3; // Equivalente a numero = numero ** 3
console.log("El nuevo valor de la variable es: " + numero);
