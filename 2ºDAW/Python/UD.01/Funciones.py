def suma(a, b):
    """Devuelve la suma de dos números.

    Args:
        a: Primer operando.
        b: Segundo operando.

    Returns:
        La suma de `a` y `b`.
    """
    return a + b


def resta(a, b):
    """Devuelve la resta de dos números (a - b).

    Args:
        a: Minuendo.
        b: Sustraendo.

    Returns:
        La diferencia entre `a` y `b`.
    """
    return a - b
